public class CekSuhu {

	public static void main(String[] args) {
		int maxsuhu = 37;
		int suhu = 36;
		
		if (suhu > maxsuhu) {
			System.out.println("Suhu tubuh anda melebihi batas maksimum suhu, Anda tidak diperbolehkan masuk!");
		}else {
			System.out.println("Suhu anda berada pada suhu normal, Anda diperbolehkan masuk");
		}

	}

}
